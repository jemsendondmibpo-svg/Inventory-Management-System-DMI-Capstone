    csvRows.push(""); // Separator
    
    // === EXECUTIVE SUMMARY ===
    csvRows.push("EXECUTIVE SUMMARY");
    csvRows.push("─────────────────────────────────────────────────────────────────────────────────────────────────────────");
    csvRows.push(""); // Separator
    
    // Summary table with proper alignment
