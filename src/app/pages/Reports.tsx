    const totalAcquired = monthlyAcquisition.reduce((sum, m) => sum + m.acquired, 0);
    const totalRetired = monthlyAcquisition.reduce((sum, m) => sum + m.retired, 0);
    const netTotal = totalAcquired - totalRetired;
    csvRows.push(`"TOTAL",${totalAcquired},${totalRetired},"${netTotal > 0 ? '+' : ''}${netTotal}"`);
