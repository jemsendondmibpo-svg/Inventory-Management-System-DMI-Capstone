    csvRows.push("DIGITAL MINDS BPO SERVICES INC.");
    csvRows.push("INVENTORY MANAGEMENT SYSTEM - COMPREHENSIVE REPORT");
    csvRows.push(`Report Generated: ${new Date().toLocaleDateString("en-US", { year: "numeric", month: "long", day: "numeric", hour: "2-digit", minute: "2-digit" })}`);
    csvRows.push(`Generated by: Jeremy Sendon (Admin)`);
    csvRows.push(""); // Separator
